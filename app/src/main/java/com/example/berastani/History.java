package com.example.berastani;

public class History {
    private long epoch;
    private int buyPera;
    private int buyPulen;
    private int stockPera;
    private int stockPulen;

    public History() {
    }

    public long getEpoch() {
        return epoch;
    }

    public void setEpoch(long epoch) {
        this.epoch = epoch;
    }

    public int getBuyPera() {
        return buyPera;
    }

    public void setBuyPera(int buyPera) {
        this.buyPera = buyPera;
    }

    public int getBuyPulen() {
        return buyPulen;
    }

    public void setBuyPulen(int buyPulen) {
        this.buyPulen = buyPulen;
    }

    public int getStockPera() {
        return stockPera;
    }

    public void setStockPera(int stockPera) {
        this.stockPera = stockPera;
    }

    public int getStockPulen() {
        return stockPulen;
    }

    public void setStockPulen(int stockPulen) {
        this.stockPulen = stockPulen;
    }
}
